package lesson_6_variables;

public class Main {

    // static variable
    static double PI = 3.14;

    //class/instance variable
    int b = 5;

    void main() {
        // local variable
        int a = 10;

        // local variable
        System.out.println(a);
        System.out.println(b);
        System.out.println(PI);

}
    void print() {
        System.out.println(b);
        System.out.println(PI);

    }
}
